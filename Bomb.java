class Bomb {
    
    static void displayInfo(String type, String explosiveMaterial, double weight, double blastRadius, double price) {
        System.out.println("\nType: " + type);
        System.out.println("Explosive Material: " + explosiveMaterial);
        System.out.println("Weight: " + weight + " kg");
        System.out.println("Blast Radius: " + blastRadius + " meters");
        System.out.println("Price: $" + price);
    }
}
